class BannerPanel(Container):

    DEFAULT_CLASSES = "banner-panel"

    def __init__(
        self,
        *children,
        title: str = "",
        version: str = "",
        model: str = "",
        system: str = "",
        path: str = "",
        **kwargs
    ):
        super().__init__(*children, **kwargs)
        self.border_title = f"{title} [gray]{version}[/]"
        self.model = model
        self.system = system
        self.path = path
    
    def compose(self) -> ComposeResult:
        yield Container(
            Label(f"[b]{LOGO}[/]"),
            id="logo"
        )
        yield Container(
            Label("[b]Tips for getting started[/]", classes="info_title"),
            Label("Run /init to create a CLAUDIO.md file with instructions for Claudio", classes="info_separator"),
            Label("[b]Settings[/]", classes="info_title"),
            Label(f"[b]Model:[/] {self.model}", id="banner_model"),
            Label(f"[b]System:[/] {self.system}"),
            Label(f"[b]Path:[/] {self.path}"),
            id="info"
        )
    
    def update_banner(self, info: dict):
        new_model = info.get("model")
        if new_model:
            model_label = self.query_one("#banner_model", Label)
            model_label.update(f"[b]Model:[/] {new_model}")